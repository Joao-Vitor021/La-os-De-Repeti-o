package PostLinkedln;

public class ExercicioLacos {
	public static void main(String[] args) {

		System.out.println("USO DO FOR");
		// Contador de 0 a 10
		for (int i = 0; i < 11; i++) {
			System.out.print(i+" ");

		}

		// Contador de nÃºmeros pares
		int n = 0;
		System.out.println("\nUSO DO WHILE");
		while (n < 20) {
			n += 2;
			System.out.print(n+" ");

		}

	}

}
